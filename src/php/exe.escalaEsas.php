          <p class="text-justify lead">A escala de avaliação de sintomas desenvolvida em Edmonton no Canadá – ESAS é um instrumento valioso nesta tarefa. Consiste num pequeno questionário com nove sintomas determinados e um décimo, de livre escolha do paciente, que passará a ser registrado diariamente. A cada sintoma solicita-se ao paciente que atribua uma nota de zero a dez, sendo zero a ausência do sintoma e dez a sua maior intensidade. O profissional deve se manter imparcial e permitir que o paciente expresse a sua própria avaliação. O ESAS inclui sintomas objetivos e subjetivos. Na impossibilidade de o paciente estabelecer uma comunicação coerente (dementados, por exemplo), a ESAS pode ser preenchida por seu cuidador com base na observação cuidadosa do seu comportamento e, neste caso, os sintomas subjetivos (cansaço, depressão, ansiedade e bem-estar) devem ser deixados em branco</p>

