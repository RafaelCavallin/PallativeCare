          <p class="text-justify lead">Por princípio, nenhum questionamento deve ser feito ao paciente se não for utilizado em seu benefício. Por isso é que os interrogatórios devem ser breves, objetivos e práticos também para a equipe. Em uidados Paliativos, escalas longas e cansativas devem ser evitadas.</p>

