			<div class="col-12">
				<p class="text-justify lead">Avaliação funcional em Cuidados Paliativos é fundamental para a vigilância da curva evolutiva da doença e se constitui em elemento valioso na tomada de decisões, previsão de prognóstico e diagnóstico da terminalidade.</p>
				<p class="text-justify lead">Existem algumas escalas de avaliação funcional que podem ser usadas em Cuidados Paliativos. Os serviços de oncologia costumam usar a escala de Karnofsky, elaborada nos anos de 1940. A escala de Karnofsky ainda é muito usada em Oncologia para a tomada de decisões.</p>  	
				<p class="text-justify lead">Em 1996 o Victoria Hospice, no Canadá, desenvolveu um instrumento de avaliação de performance, baseado no Karnofsky e adaptado aos Cuidados Paliativos. Trata-se da  “Palliative Performance Scale” – PPS. Em 2002, aperfeiçoou a escala, agregando um texto de instruções e definições.</p>		
				<p class="text-justify lead">A escala possui 11 níveis de “performance”, da 0 a 100, divididos em intervalos de 10. Ou seja, não existem valores intermediários.</p>
				<p class="text-justify lead">O PPS deve ser utilizado todos os dias para pacientes internados, em todas as consultas ambulatoriais e visitas domiciliares.</p>
				<p class="text-justify lead">A tradução oficial para o português está sendo desenvolvida por profissionais da ANCP, em São Paulo.</p>
				<p class="text-justify lead">O PPS tem sido usado na tomada de decisões em Cuidados Paliativos e parece ter algum valor prognóstico, quando associado a outros sintomas como edema, delirium, dispneia e baixa ingesta alimentar.</p>
			</div>

