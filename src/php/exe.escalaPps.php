				<p class="text-justify lead">A construção de um gráfico diário a partir dos registros do PPS tem ajudado na compreensão da evolução da doença em pacientes internados na Enfermaria de Cuidados Paliativos do HSPE desde o início de janeiro de 2009.</p>
				<h4>Exemplo:</h4>
				<p class="text-justify lead">O Sr. AS, 89 anos, com diagnóstico de Carcinoma Espinocelular de esôfago, era acompanhado no domicílio e o último registro de PPS era de 60% em 4 de janeiro, uma semana antes da internação hospitalar, que durou 19 dias. No prontosocorro, em 12/01, a queixa inicial era de confusão e sonolência e PPS de 40%, atribuído inicialmente à infecção do trato urinário. No primeiro dia na enfermaria, fez quadro de delirium agitado, seguido de torpor, caindo o PPS para 10%. O diagnóstico definitivo foi de hipercalcemia. Iniciado o tratamento, houve resposta razoável, elevando o PPS a 30%. Após 7 dias de tratamento e ajustes terapêuticos, começa a recuperar desempenho funcional e retorna a 50%, patamar ideal para alta hospitalar, em 29 de janeiro. Ver gráfico 1.</p>

