                <p class="text-justify lead"><strong>Cuidados Paliativos</strong> são a abordagem do paciente e sua família com doença ameaçadora a vida. O manejo de dor e outros sintomas, e provisão de suporte psicológico, social e espiritual são supremos, almejando alcançar a melhor qualidade de vida para pacientes e seus familiares. Muitos aspectos dos cuidados paliativos são também aplicáveis mais precocemente no curso das doenças, em conjuntura com demais tratamentos (WHO, 2002).</p>
                <p class="text-justify lead"><strong>As vantagens</strong> dos Cuidados Paliativos têm sido demonstradas na qualidade do serviço e do cuidado. Outra vantagem comprovada é o menor custo quando comparado ao tratamento convencional. Temel et al., 2010 demonstraram que os Cuidados Paliativos, quando oferecidos precocemente e corretamente aos pacientes, <strong>melhoram a qualidade de vida</strong>, o <strong>humor</strong> e a <strong>sobrevida</strong> dos pacientes. Além disso, Smith et al., 2014 demonstraram significativa <strong>redução de custos</strong> quando empregados os Cuidados Paliativos.</p>
            </div>
            <div class="col-sm-12 col-md-6">
                <p class="text-justify lead"><strong>Cuidados de fim de vida</strong> são normalmente definidos como o cuidado fornecido no último ano de vida do paciente (DoH, 2008). Entretanto, dado à fatores como a dificuldade para determinar alguns prognósticos e como as várias necessidades dos pacientes (físicas e psicológicas), os cuidados de fim de vida podem significar mais tempo para uns ou menos para outros. Considerar previamente que o paciente está se encaminhando para o fim de sua vida possibilita a eles e aos familiares pensarem sobre o que é importante para eles – o que eles iriam ou não gostar que acontecesse. Sempre se pergunte: “Eu ficaria surpreso se o paciente morrer nos próximos 6 meses?”. Se a resposta for não, então considere se é apropriado iniciar as discussões sobre o fim de vida, se os planos de conduta devem ser alterados, se outros profissionais devem ser atualizados ou se termos para proteger o paciente contra tratamentos fúteis devem ser preenchidos.</p>
